#Programme Write a Python program that accepts marks in 5 subjects and outputs average marks

m_1= int(input('How many marks did you get in your 1st subject?'))
m_2 = int(input('How many marks did you get in your 2nd subject?'))
m_3 = int(input('How many marks did you get in your 3rd subject?'))
m_4 = int(input('How many marks did you get in your 4th subject?'))
m_5 = int(input('How many marks did you get in your 5th subject?'))

